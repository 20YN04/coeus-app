// Deep-widen every string literal to `string` (and string-literal array
// elements to `string`) so the `en` dictionary below isn't forced into nl's
// literal types — both translations share shape, not exact wording.
type Widen<T> = T extends string
  ? string
  : T extends readonly (infer U)[]
    ? readonly Widen<U>[]
    : T extends object
      ? { [K in keyof T]: Widen<T[K]> }
      : T;

